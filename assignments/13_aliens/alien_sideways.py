import pygame
from pygame.sprite import Sprite

# Image used is made my AI

class Alien(Sprite):
    '''A class to represent a single alien in the fleet'''

    def __init__(self, ai_game):
        '''Initialize the alien and set its starting position.'''
        super().__init__()
        self.screen = ai_game.screen
        self.settings = ai_game.settings

        # Load the alien image and set its rect attribute.
        self.image = pygame.image.load("C:/Users/bre30/OneDrive/Desktop/csc 141/13_aliens/images/alien.bmp")
        self.rect = self.image.get_rect()

        # Start each new alien near the top left of the screen.
        self.rect.y = self.rect.width
        self.rect.x = self.rect.height

        # Store the alien's exact horizontal position.
        self.y = float(self.rect.y)
        
    def check_edges(self):
        """Return True if alien is at edge of screen."""
        screen_rect = self.screen.get_rect()
        if self.rect.top <= 0 or self.rect.bottom >= self.settings.screen_height:
            return True
        return False
            
    
    def update(self):
        """Move the alien vertically"""
        self.rect.y += self.settings.alien_speed * self.settings.fleet_direction
       




        